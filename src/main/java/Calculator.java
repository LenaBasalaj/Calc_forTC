public class Calculator {

    public int calcSum (int x, int y)
    {
        return x+y;
    }
    public double calcSumDouble (double x, double y)
    {
        return x+y;
    }

    public int calcSub (int x, int y)
    {
        return x-y;
    }
    public double calcSubDouble (double x, double y)
    {
        return x-y;
    }

    public int calcMult (int x, int y)
    {
        return x*y;
    }

    public double calcMultDouble (double x, double y)
    {
        return x*y;
    }
    public double calcDiv (double x, double y)
    {
        return x/y;
    }
    public double calcPercent (double x, double y)
    {
        return x*y/100;
    }


}
